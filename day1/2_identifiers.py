# what are keywords
# keywords are the researved words in python which have their special meaning
# there are 35 keywords in python

# what are identifiers
# identifiers are the user-defined names while writing codes
# variable name, function name, class name, file name, etc are the identifiers

# rules for naming identifiers
#1. identifiers cant start with a number except for file name
#2. they should always start with a-z or A-Z
#3. identifiers are case-sensitive
# for eg. a and A are two different variables
#4. identifiers cant contain special symbols like !, @, #, etc
#5. identifiers cant be named after keywords
#6. identifiers can contain "_"

